export function getMenuItemById(id: string): MenuItem | undefined {
  return mockMenuItems.find(item => item.id === id);
}

export function getMenuItemsByType(type: MenuItem['type']): MenuItem[] {
  return mockMenuItems.filter(item => item.type === type);
}

export function getPromoItems(): MenuItem[] {
  return mockMenuItems.filter(item => item.isOnSale || (item.promoType !== null && item.promoType !== undefined));
}
