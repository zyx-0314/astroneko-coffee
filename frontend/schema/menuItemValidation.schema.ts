import { z } from "zod";

// Zod schema for menu item validation
export const MenuItemValidationSchema = z.object({
  name: z
    .string()
    .min(1, "Name is required")
    .min(2, "Name must be at least 2 characters")
    .max(100, "Name must be less than 100 characters")
    .trim(),
  price: z
    .string()
    .min(1, "Price is required")
    .refine((val) => !isNaN(Number(val)) && Number(val) > 0, {
      message: "Price must be a positive number",
    })
    .transform((val) => Number(val)),
});

// Type inference from Zod schema
export type MenuItemFormData = z.infer<typeof MenuItemValidationSchema>;

// Schema for form validation (string inputs)
export const MenuItemFormSchema = z.object({
  name: z
    .string()
    .min(1, "Name is required")
    .min(2, "Name must be at least 2 characters")
    .max(100, "Name must be less than 100 characters")
    .trim(),
  price: z
    .string()
    .min(1, "Price is required")
    .refine((val) => !isNaN(Number(val)) && Number(val) > 0, {
      message: "Price must be a positive number",
    }),
});

export type MenuItemFormInput = z.infer<typeof MenuItemFormSchema>;
